    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String fullname = txtfullname.getText();
        String username = txtusername.getText();
        String pass = txtpassword.getText();
        
        String sql="Insert into users values('"+username+"','"+pass+"','"+fullname+"')";
        
        try{
            dbconn.St.execute(sql);
            JOptionPane.showMessageDialog(this, fullname+" has been registered");
            new LogIn_Form().setVisible(true);
            this.hide();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        txtusername.setText("");//clears the text fields after entry has been saved
        txtfullname.setText("");
        txtpassword.setText("");
        
    }//GEN-LAST:event_btnRegisterActionPerformed

